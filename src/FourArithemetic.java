public class FourArithemetic {
    public static void main(String[] args)
    {
        int a,b, sum, cha, goeb, nanu;
        a = 12;
        b = 2;
        sum = a + b;
        cha = a-b;
        goeb = a*b;
        nanu = a/b;

        System.out.println("a+b=" + sum);
        System.out.println(cha);
        System.out.println(goeb);
        System.out.println(nanu);
    }
}
